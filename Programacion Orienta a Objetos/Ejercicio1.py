class Animal:

  def __init__(self, animal, acciones):
      self.animal = animal
      self.acciones = acciones

  def camina(self):
       print("Estoy caminando")

  def come(self):
       print("Estoy comiendo")

  def comunico(self):
       print("Me comunico")

  def cadena(self):
       print("Estoy en la cadena alimenticia")

  def vivo(self):
       print("Tengo vida")

oso = Animal("oso","carnivoro")

print(oso.animal)
print(oso.acciones)
oso.camina()
oso.come()
oso.comunico()
oso.cadena()
oso.vivo()

